package lec01basic;

public class FormattedOutput2 {
    public static void main(String[] args) {
        System.out.println(3 + 4);//Integer
        System.out.println(3 + "4");//Integer=>String
        System.out.println("3" + "4");//Ctrl+Alt+L
        System.out.println(1 + 2 + 3);
        System.out.println(1 + 2 + "3");
        System.out.println("1" + 2 + 3);

    }
}
